    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        try {
            int index = tableDanhSachBan.getSelectedRow();
            if (index == -1) {
                throw new IOException("Bạn chưa chọn ban muốn xoá!");
            }
            if (JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xoá ban " + dsBanThuocPhong.get(index).getTenBan() + " ?", "Cảnh báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                BanModel banXoa = dsBanThuocPhong.get(index);
                dsBan.remove(banXoa);
                BanController.WriteToFile(dsBan);
                XoaBan_NhanSuTheoBan(banXoa);
                
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Xoa thất bại!", JOptionPane.ERROR_MESSAGE);
        }
        try {
            XemBan f = new XemBan(id, thuocPhong);
            f.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            f.setLocationRelativeTo(null);
            f.setResizable(false);
            f.setVisible(true);
            this.dispose();
        } catch (IOException ex) {
            Logger.getLogger(XemBan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_XoaActionPerformed

