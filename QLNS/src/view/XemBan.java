    public XemBan(String id, PhongModel thuocPhong) throws IOException {
        initComponents();
        this.id = id;
        this.thuocPhong = thuocPhong;
        try {
            dsBan = BanController.ReadToFile();
            dsBan_NhanSu = Ban_NhanSuController.ReadToFile();
            if(thuocPhong != null){
                dsBanThuocPhong = BanController.TimBanTheoPhong(dsBan, thuocPhong);
                text_Phong.setText(text_Phong.getText() + thuocPhong.getTenPhong());
            }
            else {
                dsBanThuocPhong.addAll(dsBan);
                text_Phong.setText(text_Phong.getText() + "Tat ca phong");
            }
        } catch (IOException ex) {

        }
        model = (DefaultTableModel) tableDanhSachBan.getModel();
        showresult();
    }

    public void showresult() {
        for (BanModel s : dsBanThuocPhong) {
            model.addRow(new Object[]{
                s.getMaBan(), s.getTenBan(), Ban_NhanSuController.TimNhanSuTheoBan(dsBan_NhanSu, s).size(), s.getThuocPhong().getTenPhong()
            });
        }
    }
    public void XoaBan_NhanSuTheoBan(BanModel banXoa) {
        try {
            ArrayList<Ban_NhanSuModel> list = new ArrayList<>();
            list.addAll(dsBan_NhanSu);
            for (int i = 0; i < list.size(); i++) {
                if (list.get(i).getBan().equals(banXoa)) {
                    list.remove(list.get(i));
                    i--;
                }
            }
            Ban_NhanSuController.WriteToFile(list);
        } catch (IOException e) {
            System.out.println("Loi b");
        }
    }
    

