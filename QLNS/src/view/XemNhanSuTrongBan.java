    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        try {
            int index = tableDanhSachNhanSu.getSelectedRow();
            if (index == -1) {
                throw new IOException("Bạn chưa chọn nhanSu muốn xoá!");
            }
            Ban_NhanSuModel nhanSuMuonXoa = dsBan_NhanSuThuocBan.get(index);
            if (JOptionPane.showConfirmDialog(null, "Bạn muốn xoá nhan su khoi ban ? \n"
                    + nhanSuMuonXoa.getNhanSu().getHoTen()
                    + "\nBan: " + nhanSuMuonXoa.getBan().getTenBan()
                    + "\nChuc vu: " + nhanSuMuonXoa.getChucVu() + "\n", "Cảnh báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                
                dsBan_NhanSu.remove(dsBan_NhanSuThuocBan.get(index));
                Ban_NhanSuController.WriteToFile(dsBan_NhanSu);
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Xoa thất bại!", JOptionPane.ERROR_MESSAGE);
        }
        try {
            XemNhanSuTrongBan f = new XemNhanSuTrongBan(id, thuocBan, nhanSu);
            f.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            f.setLocationRelativeTo(null);
            f.setResizable(false);
            f.setVisible(true);
            this.dispose();
        } catch (IOException ex) {
            Logger.getLogger(XemNhanSuTrongBan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_XoaActionPerformed

