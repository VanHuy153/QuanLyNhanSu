    public XemNhanSuTrongBan(String id, BanModel thuocBan, NhanSuModel nhanSu) throws IOException {
        initComponents();
        this.id = id;
        this.thuocBan = thuocBan;
        this.nhanSu = nhanSu;
        try {
            dsBan_NhanSu = Ban_NhanSuController.ReadToFile();
            if (nhanSu == null) {
                if (!thuocBan.getMaBan().equals("")) {
                    dsBan_NhanSuThuocBan = Ban_NhanSuController.TimNhanSuTheoBan(dsBan_NhanSu, thuocBan);
                    text_Ban.setText(text_Ban.getText() + thuocBan.getTenBan());
                    text_Phong.setText(text_Phong.getText() + thuocBan.getThuocPhong().getTenPhong());
                } else if (thuocBan.getThuocPhong() != null) {
                    ArrayList<BanModel> dsBanThuocPhong = BanController.TimBanTheoPhong(BanController.ReadToFile(), thuocBan.getThuocPhong());
                    for (BanModel b : dsBanThuocPhong) {
                        dsBan_NhanSuThuocBan.addAll(Ban_NhanSuController.TimNhanSuTheoBan(dsBan_NhanSu, b));
                    }
                    text_Ban.setText(text_Ban.getText() + "Tat ca ban");
                    text_Phong.setText(text_Phong.getText() + thuocBan.getThuocPhong().getTenPhong());
                } else {
                    dsBan_NhanSuThuocBan.addAll(dsBan_NhanSu);
                    text_Ban.setText(text_Ban.getText() + "Tat ca ban");
                    text_Phong.setText(text_Phong.getText() + "Tat ca phong");
                }
            } else {
                dsBan_NhanSuThuocBan = Ban_NhanSuController.TimBanTheoNhanSu(dsBan_NhanSu, nhanSu);
                text_Ban.setText(text_Ban.getText() + "Tat ca ban");
                text_Phong.setText(text_Phong.getText() + "Tat ca phong");
            }
        } catch (IOException ex) {

        }
        model = (DefaultTableModel) tableDanhSachNhanSu.getModel();
        showresult();
    }

    public void showresult() {
        for (Ban_NhanSuModel s : dsBan_NhanSuThuocBan) {
            model.addRow(new Object[]{
                s.getBan().getThuocPhong().getTenPhong(),
                s.getBan().getTenBan(),
                s.getNhanSu().getHoTen(),
                s.getNhanSu().getChuyenNganh(),
                s.getChucVu()
            });
        }
    }

